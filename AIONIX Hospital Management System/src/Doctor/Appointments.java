    private void dconsultantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dconsultantActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) t.getModel();
        for(int i=0;i<20;i++)
        {
            model.setValueAt("", i, 1);
            model.setValueAt("", i, 0);
        }
        Object c=model.getValueAt(1, 2);
        date=c.toString();
        doc=(String) dconsultant.getSelectedItem();
        if(doc.equals("--------------Select--------------"))
        {
            JOptionPane.showMessageDialog(rootPane, "Select a Doctor");
        }
        else
        {
            try{
                Class.forName("java.sql.DriverManager");
                Connection con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/aionix","root","");
                Statement stmt=(Statement) con.createStatement();
                String query="Select * from Appointments where DName='"+doc+"' AND Date='"+date+"';";
                ResultSet rs=stmt.executeQuery(query);
                while(rs.next())
                {
                    Object ll=rs.getTime("Time");
                    time=ll.toString();
                    for(int i=0;i<20;i++)
                    {
                        Object d=model.getValueAt(i, 3);
                        String tt=d.toString()+":00";
                        if(time.equals(tt))
                        {
                            no=""+rs.getInt("SNo");
                            model.setValueAt(no, i, 0);
                            namep=rs.getString("PName");
                            model.setValueAt(namep, i, 1);
                        }
                    }
                }
            }catch(Exception Ex){}
        }
}//GEN-LAST:event_dconsultantActionPerformed

