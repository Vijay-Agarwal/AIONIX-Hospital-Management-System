    private void edicanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edicanActionPerformed
        // TODO add your handling code here:
        edic=edican.getText();
        if(edic.equals("Edit"))
        {
            if(dconsultant.getSelectedItem()=="--------------Select--------------")
            {
               JOptionPane.showMessageDialog(rootPane, "Select a Doctor");
            }
            else
            {
                adsave.setText("Save");
                dconsultant.setEnabled(false);
                adsave.setEnabled(false);
                edican.setText("Cancel");
                h++;
            }
        }
        else
        {
            if(h==1)
            {
                DefaultTableModel m=(DefaultTableModel) t.getModel();
                m.setValueAt(old, select, 1);
                adsave.setText("Add");
                adsave.setEnabled(true);
                g=0;
                edic="";
                adsav="";
                dconsultant.setEnabled(true);
                edican.setText("Edit");
                h=0;
            }
            else
            {
                DefaultTableModel m=(DefaultTableModel) t.getModel();
                m.setValueAt("", select, 1);
                adsave.setText("Add");
                adsave.setEnabled(true);
                g=0;
                edic="";
                adsav="";
                dconsultant.setEnabled(true);
                edican.setText("Edit");
            }
        }
}//GEN-LAST:event_edicanActionPerformed

